
def add(a,b):
    answer = a+b
    return answer

def subtract(a,b):
    subtracted_result = a-b
    return subtracted_result

def divide(a,b):
    divided_result = a/b
    return divided_result

def multiply(a,b):
    multiplied_result = a*b
    return multiplied_result

def remainder(a,b):
    divided_result = a%b
    return divided_result